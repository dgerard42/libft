#include "libft.h"

int			ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int i;
	int match;

	i = 0;
	match = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < (int)n)
		i++;
	return (s1[i] - s2[i]);
}
